export type KalepIconStem = keyof typeof kalepIconUrls

export function kalepIconUrl(stem: KalepIconStem): string {
  return kalepIconUrls[stem]
}

function tabPair(defaultStem: KalepIconStem, selectedStem: KalepIconStem) {
  return { default: kalepIconUrl(defaultStem), selected: kalepIconUrl(selectedStem) }
}

